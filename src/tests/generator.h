std::string RandStr(int len) {
  std::random_device rd;
  std::default_random_engine engine(rd());
  std::uniform_int_distribution<char> distr(0, 122);

  std::string str;
  str.reserve(len + 1);

  for (int i = 0; i < len; ++i) {
    int a = distr(engine) % 3;
    int cc = ((int)distr(engine) % 26);
    switch (a) {
      case 0:
        str += (char)(cc + 'A');
        //        std::cout << ' ' <<(int) cc;
        break;
      case 1:
        str += (char)(cc + 'a');
        break;
      case 2:
        str += (char)(cc % 9 + '0');
        break;
    }
  }
  return str;
}

